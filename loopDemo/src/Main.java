public class Main {
    public static void main(String[] args) {
        //for döngüsü
        for (int i = 0; i <= 100; i++) {
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti");
        //while döngüsü
        int i = 1;
        while (i < 10) {
            System.out.println(i);
            i++;

        }
        System.out.println("While Döngüsü Bitti");
    }
}